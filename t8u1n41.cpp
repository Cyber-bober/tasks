#include <iostream>
using namespace std;

int main() {
    int a, b;

    cout << "Введите начало отрезка (a): ";
    cin >> a;
    cout << "Введите конец отрезка (b): ";
    cin >> b;

    // Проверка на корректность диапазона
    if (a > b) {
        cout << "Ошибка: a должно быть меньше или равно b." << endl;
        return 1;
    }

    cout << "Сумма цифр для каждого числа от " << a << " до " << b << ":\n";

    // Вывод суммы цифр для каждого числа в диапазоне
    for (int i = a; i <= b; ++i) {
        int sum = 0; // Обнуляем сумму для текущего числа
        int n = i; // Делаем копию числа

        // Считаем сумму цифр
        while (n > 0) {
            sum += n % 10; // Берем последнюю цифру
            n /= 10; // Убираем последнюю цифру
        }

        cout << "Сумма цифр числа " << i << " = " << sum << endl;
    }

    return 0;
}